    case partlyCloudyDark = "PartlyCloudyDark"
    case cloudy = "Cloudy"
    case cloudyDark = "CloudyDark"
    case mist = "Mist"
    case mistDark = "MistDark"
    case snow = "Snow"
    case snowDark = "SnowDark"
    case freezingRain = "FreezingRain"
    case freezingRainDark = "FreezingRainDark"
    case heavyRain = "HeavyRain"
    case heavyRainDark = "HeavyRainDark"
    case partlyCloudyRain = "PartlyCloudyRain"
    case partlyCloudyRainDark = "PartlyCloudyRainDark"
    case lightRain = "LightRain"
    case lightRainDark = "LightRainDark"
    case thunderstorm = "Thunderstorm"
    case thunderstormDark = "ThunderstormDark"
}
